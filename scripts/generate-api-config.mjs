#!/usr/bin/env node
// Generate API configuration files from Keystatic YAML sources.
//
// Reads:  source/pages/contact.yaml, source/site/perusal-access.yaml, source/site/score-pdf.yaml
// Writes: api/.env.validation       (form field constraints)
//         api/email-templates.json   (email template text with {{token}} placeholders)
//
// Both files are committed to git (like other generated data) so the PHP API
// always has valid values present without requiring a build step.
// Regenerated whenever `npm run generate:data` runs (dev + build pipeline).

import { readFileSync, writeFileSync, existsSync, statSync, copyFileSync } from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { load } from 'js-yaml'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')

function loadYaml(relPath) {
  const fullPath = path.join(root, relPath)
  try {
    return load(readFileSync(fullPath, 'utf8')) ?? {}
  } catch {
    return {}
  }
}

const contact = loadYaml('source/pages/contact.yaml')
const perusal = loadYaml('source/site/perusal-access.yaml')
const scorePdf = loadYaml('source/site/score-pdf.yaml')
const emailLayout = loadYaml('source/site/email-layout.yaml')

// ── .env.validation (form field constraints) ─────────────────────────────────

// Use explicit LF (\n) line endings — never os.EOL — so the file is safe on
// Linux servers regardless of whether this script runs on macOS or Windows.
const lines = [
  '# Auto-generated from Keystatic YAML — do not edit manually.',
  '# Regenerated by: npm run generate:data',
  '',
  `CONTACT_NAME_MAX_LENGTH=${Number.isInteger(contact.nameMaxLength) ? contact.nameMaxLength : 120}`,
  `CONTACT_MESSAGE_MAX_LENGTH=${Number.isInteger(contact.messageMaxLength) ? contact.messageMaxLength : 4000}`,
  `PERUSAL_NAME_MAX_LENGTH=${Number.isInteger(perusal.nameMaxLength) ? perusal.nameMaxLength : 120}`,
  '',
  '# PDF download filename template (from source/site/score-pdf.yaml)',
  `DOWNLOAD_FILENAME_FORMAT="${typeof scorePdf.downloadFilenameFormat === 'string' && scorePdf.downloadFilenameFormat ? scorePdf.downloadFilenameFormat : '{{composerName}} -- {{workTitle}} {{workSubtitle}} -- {{suffix}}'}"`,
  `DOWNLOAD_WATERMARKED_SUFFIX="${typeof scorePdf.downloadWatermarkedSuffix === 'string' ? scorePdf.downloadWatermarkedSuffix : 'PERUSAL SCORE'}"`,
  `DOWNLOAD_ORIGINAL_SUFFIX="${typeof scorePdf.downloadOriginalSuffix === 'string' ? scorePdf.downloadOriginalSuffix : ''}"`,
  '',
  '# Email layout (from source/site/email-layout.yaml)',
  `EMAIL_SHOW_HEADER_FAVICON=${emailLayout.showHeaderFavicon !== false}`,
  `EMAIL_SHOW_SIGNATURE_LOGO=${emailLayout.showSignatureLogo !== false}`,
  `EMAIL_SIGNATURE_LOGO_WIDTH=${Number.isInteger(emailLayout.signatureLogoWidth) ? emailLayout.signatureLogoWidth : 160}`,
]

const envPath = path.join(root, 'api', '.env.validation')
writeFileSync(envPath, lines.join('\n') + '\n', { encoding: 'utf8' })
console.log('[generate-api-config] Wrote api/.env.validation')

// ── email-templates.json (email body/subject templates) ──────────────────────

const templates = {
  perusalSubject: typeof perusal.emailSubject === 'string' && perusal.emailSubject
    ? perusal.emailSubject
    : 'Your perusal score, {{firstName}} — {{workTitle}}',
  perusalBody: typeof perusal.emailMessage === 'string' && perusal.emailMessage
    ? perusal.emailMessage
    : '',
  perusalNotificationSubject: typeof perusal.notificationSubject === 'string' && perusal.notificationSubject
    ? perusal.notificationSubject
    : 'New score request from {{firstName}} via {{siteDomain}}',
  contactAutoReplySubject: typeof contact.autoReplySubject === 'string' && contact.autoReplySubject
    ? contact.autoReplySubject
    : 'Thank you for your message — {{composerName}}',
  contactAutoReplyBody: typeof contact.autoReplyMessage === 'string' && contact.autoReplyMessage
    ? contact.autoReplyMessage
    : '',
}

const jsonPath = path.join(root, 'api', 'email-templates.json')
writeFileSync(jsonPath, JSON.stringify(templates, null, 2) + '\n', { encoding: 'utf8' })
console.log('[generate-api-config] Wrote api/email-templates.json')

// ── Email logo (favicon → api/ for CID inline attachment) ───────────────────

const faviconSrc = path.join(root, 'source', 'branding', 'favicon-96x96.png')
const emailLogoDest = path.join(root, 'api', 'email-logo.png')

if (existsSync(faviconSrc)) {
  const needsCopy =
    !existsSync(emailLogoDest) ||
    statSync(faviconSrc).mtimeMs > statSync(emailLogoDest).mtimeMs ||
    statSync(faviconSrc).size !== statSync(emailLogoDest).size

  if (needsCopy) {
    copyFileSync(faviconSrc, emailLogoDest)
    console.log('[generate-api-config] Copied email logo → api/email-logo.png')
  } else {
    console.log('[generate-api-config] api/email-logo.png up to date')
  }
} else {
  console.log('[generate-api-config] No favicon-96x96.png found, skipping email logo')
}
